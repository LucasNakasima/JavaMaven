package Aep2;

public class Avaliacao {
	private String avaliacao;
	private String data;
	
	public Avaliacao(String avaliacao, String data) {
		this.avaliacao = avaliacao;
		this.data = data;
	}
	
	public String getAvaliacao() {
		return avaliacao;
	}
	
	public String getData() {
		return data;
	}
}
